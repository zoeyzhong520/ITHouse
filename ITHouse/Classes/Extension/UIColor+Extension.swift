        return RGB(185,53,44)
    }
    
    ///BlackTextColor (89,89,89)
    static var blackTextColor: UIColor {
        return RGB(89,89,89)
    }
    
    ///GrayTextColor (170,170,170)
    static var grayTextColor: UIColor {
        return RGB(170,170,170)
    }
}
