import UIKit

extension UIFont {
    
    ///导航栏标题字体
    static var navTitleFont: UIFont {
        return NormalFont(15)
    }
    
    ///标题字体
    static var titleFont: UIFont {
        return NormalFont(13)
    }
    
    ///正文字体
    static var textFont: UIFont {
        return NormalFont(11)
    }
}
